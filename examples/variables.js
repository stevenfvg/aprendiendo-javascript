// Existen 3 formas de crear variables en JavaScript, en nuevas versiones solo se utilizan 2:
// Declaración de variables con let
let variableLet = 'Soy una variable declarada con let';
console.log(variableLet);

// Reasignación de variable let
variableLet = 'He sido reasignada con let';
console.log(variableLet);

// Declaración de variables con const
const variableConst = 'Soy una variable declarada con const';
console.log(variableConst);

